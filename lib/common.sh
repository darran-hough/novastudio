install_pkg(){
 for pkg in "$@"; do
   if ! rpm -q "$pkg" &>/dev/null; then
      log "Installing $pkg"
      dnf install -y --allowerasing "$pkg"
   fi
 done
}

load_modules(){
 MODULES=(
  hardware
  profile
  gpu
  audio
  gaming
  wine
  media
  optimisations
 )

 for m in "${MODULES[@]}"; do
   source "$BASE_DIR/modules/$m.sh"
 done
}

run_modules(){
 for m in "${MODULES[@]}"; do
   log "Running module: $m"
   "module_$m"
 done
}
